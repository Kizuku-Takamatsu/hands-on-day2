# MAGIC ## 5. SQL の「超」入門（今日いちばん使います）
# MAGIC
# MAGIC SQL は「**どんなデータが欲しいか**」を書くための言葉です。下の5語だけ分かれば、今日のSQLはほぼ読めます。
# MAGIC
# MAGIC | キーワード | 意味（日本語で言うと） |
# MAGIC |---|---|
# MAGIC | `SELECT` | **どの列が欲しい**か（例：売上、件数） |
# MAGIC | `FROM` | **どの表**から取るか（例：受注テーブル） |
# MAGIC | `WHERE` | **どの行に絞る**か（例：2026年だけ） |
# MAGIC | `GROUP BY` | **何ごとにまとめる**か（例：セグメントごと） |
# MAGIC | `ORDER BY` | **どう並べる**か（例：売上が多い順） |
# MAGIC
# MAGIC ### 読み解きの例（今日の教材より）
# MAGIC ```sql
# MAGIC SELECT market_segment, sum(order_amount)   -- 「セグメント」と「売上合計」が欲しい
# MAGIC FROM   orders_silver                        -- 受注テーブルから
# MAGIC GROUP BY market_segment                     -- セグメントごとにまとめて
# MAGIC ORDER BY sum(order_amount) DESC             -- 売上が多い順に並べる
# MAGIC ```
# MAGIC > 日本語にすると「**受注テーブルから、セグメントごとの売上合計を、多い順に出して**」。それだけです。

